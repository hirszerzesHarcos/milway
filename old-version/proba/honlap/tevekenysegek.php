        <li>Az automaták megfigyelése kamerarendszerrel, a rongálások és nem kívánt atrocitások kivédése miatt.</li>
      </ul>
      <p>Az automaták kihelyezésének menete:</p>
      <ul>
        <li>A terület felmérése. A kollega kimegy a helyszínre és felméri a terepet. Felméri, hogy a meglévő szerződéses feltételeknek megfelel-e a helyszín.</li>
        <li>Az automaták szállítása. Az automatákat kiszállítjuk, és ha kell a megfelelő emberekkel a helyére tesszük a gépeket.</li>
        <li>Az automaták beszerelése. A technikus kollega felkészíti és beszereli a gépet az adott helyen. A szerződésnek megfelelően.</li>
        <li>Az automata feltöltése. A technikus kollega feltölti a megfelelő áruval a gépeket. A szerződésben foglalt a termékek kerülnek a gépbe.</li>
      </ul>
      <p>Az üzemeltetés menete mindig az aktuális fogyási mutatóktól függnek. Ilyenkor egy bevált időzónában jár a töltő feltölteni és tisztítani a gépeket. Ugyan ezt teszi a technikus is, egy bevált rendszer szerint játja a gépeket és karbantartja, ha kell alkatrészt is cserél és tisztítja az automatákat.</p>
      <h2>Automaták szervízelése, karbantartása</h2>
      <p>Az automaták nagy része telemetriával rendelkeznek. Emiatt mi a távfelügyelet segítségével ki tudjuk szeparálni a nem megfelelően működő gépet és azt gyakorlatilag 24 órán belül helyre is állítjuk. A hiba bejelentés történhet a megszokott módon is, vagyis telefonon és e-mail-on is keresztül. A károsult vásárlót, ha a gép károsította meg, minden esetben megtérítjük és egy plusz termékkel is kedveskedünk a károsult ügyfél részére. Hiszen az elégedett vásárló mindent megér.</p>
      <h2>Automaták bérbeadása, forgalmazása</h2>
      <p>Az automatákat igény szerint állítjuk be és készítjük fel, a bérbeadási szerződés feltételeknek megfelelően. Félév garanciával és ingyenes kiszállással biztosítjuk a bérbe adott gépeink kifogástalan működését.</p>
      <p>Az eladott gépek teljes körű átvizsgálásban, vagy felújításban vesznek részt attól függően, hogy a kedves vásárló, milyen formában szeretné az automatát megvásárolni. A megvásárlást követően féléves teljes garanciát vállalunk abban az esetben, ha a gép megfelelően lett üzemeltetve.</p>

